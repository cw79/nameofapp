class Bar
	def initialize(id, image_key, name, characteristics)
		@id = id
		@image_key = image_key
		@name = name
		@characteristics = characteristics
	end

	def id
		return @id
	end

	def image_key
		return @image_key
	end

	def name
		return @name
	end

	def characteristics
		return @characteristics
	end
end